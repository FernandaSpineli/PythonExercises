import math

angulo = float(input('Digite o 창ngulo: '))

seno = math.sin(math.radians(angulo))
print('O seno do 창ngulo tem valor {:.2f}.' .format(seno))

cosseno = math.cos(math.radians(angulo))
print('O cosseno do 창ngulo tem valor {:.2f}.' .format(cosseno))

tangente = math.tan(math.radians(angulo))
print('A tagente do 창ngulo tem valor {:.2f}.' .format(tangente))